    private class MyModel extends AbstractTableModel {
        List<Teacher> list;
        MyModel(List<Teacher> list) {
            this.list = list;
        }

        @Override
        public String getColumnName(int column) {
            switch (column) {
                case 0 -> {
                    return "Mã số";
                }
                case 1 -> {
                    return "Họ và tên";
                }
                case 2 -> {
                    return "Số điện thoại";
                }
                case 3 -> {
                    return "Giới tính";
                }
                default -> {
                    return "Ngày sinh";
                }
            }
        }
        
        
        @Override
        public int getColumnCount() {
            return 5;
        }

        @Override
        public int getRowCount() {
            return list.size();
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Teacher teacher = list.get(rowIndex);
            switch(columnIndex) {
                case 0 -> {
                    return teacher.getId();
                }
                case 1 -> {
                    return teacher.getName();
                }
                case 2 -> {
                    return teacher.getPhoneNumber();
                }
                case 3 -> {
                    return teacher.isMale() ? "Nam" : "Nữ";
                }
                default -> {
                    return teacher.getBirthday();
                }
            }
        }
    }

