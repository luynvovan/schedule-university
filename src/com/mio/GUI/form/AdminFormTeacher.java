    private void btnTeacherAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTeacherAddActionPerformed
        Object[] o = new UserInforDialog(Main.getInstance(), null, User.UserRole.TEACHER).showDialog();
        if(o != null) {
            Teacher t = (Teacher)o[0];
            t.setDepartmentId(dep.getId());
            User u = new User(-1, (String)o[1], User.UserRole.TEACHER);
            
            if(TeacherBLL.getInstance().addTeacher(u, t)) {
                list.add(t);
                ((MyModel)table.getModel()).fireTableRowsInserted(list.size() - 1, list.size() - 1);
            } else {
                Main.getInstance().showNotification(Main.NotificationType.ADD_FAILED);
            }
        }

    }//GEN-LAST:event_btnTeacherAddActionPerformed

