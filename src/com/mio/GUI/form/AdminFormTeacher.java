    private void btnTeacherEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTeacherEditActionPerformed
        int r = table.getSelectedRow();
        if(r != -1) {
            Teacher t = list.get(r);
            Object[] o = new UserInforDialog(Main.getInstance(), t, User.UserRole.TEACHER).showDialog();
            if(o != null) {
                Teacher edit = (Teacher)o[0];
                edit.setId(t.getId());
                edit.setDepartmentId(t.getDepartmentId());
                if(TeacherBLL.getInstance().updateTeacher(edit)) {
                    list.get(r).copy(edit);
                    ((MyModel)table.getModel()).fireTableRowsUpdated(r, r);
                } else {
                    Main.getInstance().showNotification(Main.NotificationType.UPDATE_FAILED);
                }
            }
        }
    }//GEN-LAST:event_btnTeacherEditActionPerformed

    private void btnTeacherDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTeacherDelActionPerformed
        int r = table.getSelectedRow();
        if(r != -1) {
            if(TeacherBLL.getInstance().delTeacher(list.get(r).getId())) {
                UserBLL.getInstance().deleteUser(list.get(r).getId());
                list.remove(r);
                ((MyModel)table.getModel()).fireTableRowsDeleted(r, r);
            } else {
                Main.getInstance().showNotification(Main.NotificationType.DELETE_FAILED);
            }
        }
    }//GEN-LAST:event_btnTeacherDelActionPerformed


