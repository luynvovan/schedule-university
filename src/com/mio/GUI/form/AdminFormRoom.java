    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        Room r = new Room(-1, tfName.getText(), (int)spnSize.getValue(), rbIsLab.isSelected());
        RoomBLL.getInstance().addRoom(r);
        if(r.getId() != -1) {
            list.add(r);
            ((AbstractTableModel)table.getModel()).fireTableRowsInserted(list.size() - 1, list.size() - 1);
        } else {
             Main.getInstance().showNotification(Main.NotificationType.ADD_FAILED);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int row = table.getSelectedRow();
        if(row != -1) {
            Room r = new Room(list.get(row).getId(), tfName.getText(), (int)spnSize.getValue(), rbIsLab.isSelected());
            if(RoomBLL.getInstance().updateRoom(r)) {
                list.get(row).copy(r);
                ((AbstractTableModel)table.getModel()).fireTableRowsUpdated(row, row);
            } else {
                Main.getInstance().showNotification(Main.NotificationType.UPDATE_FAILED);           
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        int row = table.getSelectedRow();
        if(row != -1) {
            if(RoomBLL.getInstance().delRoom(list.get(row).getId())) {
                list.remove(row);
                ((AbstractTableModel)table.getModel()).fireTableRowsDeleted(row, row);
            } else {
                Main.getInstance().showNotification(Main.NotificationType.DELETE_FAILED);
            }
        }
    }//GEN-LAST:event_btnDelActionPerformed


