        private class MyModel extends AbstractTableModel {
        List<Room> list;
        MyModel(List<Room> list) {
            this.list = list;
        }

        @Override
        public String getColumnName(int column) {
            switch (column) {
                case 0 -> {
                    return "Mã số";
                }
                case 1 -> {
                    return "Tên phòng";
                }
                case 2 -> {
                    return "Sức chứa";
                }
                default -> {
                    return "Loại phòng";
                }
            }
        }
        
        
        @Override
        public int getColumnCount() {
            return 4;
        }

        @Override
        public int getRowCount() {
            return list.size();
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Room s = list.get(rowIndex);
            switch(columnIndex) {
                case 0 -> {
                    return s.getId();
                }
                case 1 -> {
                    return s.getName();
                }
                case 2 -> {
                    return s.getSize();
                }
                default -> {
                    return s.isLab() ? "Phòng máy" : "Phòng thường";
                }
            }
        }

        @Override
        public boolean isCellEditable(int rowIndex, int columnIndex) {
            return false;
        }
    }     
