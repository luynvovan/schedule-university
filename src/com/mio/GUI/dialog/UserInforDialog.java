    public UserInforDialog(java.awt.Frame parent, Object o, User.UserRole role) {
        super(parent, true);
        
        initComponents();
        setBackground(new Color(0, 0, 0, 0));
        
        this.role = role;
        
        addMouseListener(new MouseAdapter() {
            @Override
            public void mousePressed(MouseEvent e) {
                x = e.getX();
                y = e.getY();
            }
            
        });
        
        UserInforDialog me = this;
        addMouseMotionListener(new MouseMotionAdapter() {
            @Override
            public void mouseDragged(MouseEvent e) {
                me.setLocation(e.getXOnScreen() - x, e.getYOnScreen() - y);
            }
        });
        
        
        DateChooser dc = new DateChooser();
        dc.setDateFormat("yyyy-MM-dd");
        dc.setTextRefernce(tfBirthday);
        dc.setForeground(btnOk.getBackground());
        cbxGender.addItem("Nam");
        cbxGender.addItem("Nữ");
        
        // edit
        if(o != null){
            
            tfUsername.setEnabled(false);
            switch (role) {
                case TEACHER -> {
                    Teacher t = (Teacher)o;
                    tfUsername.setText(UserBLL.getInstance().getUsername(t.getId()));
                    tfName.setText(t.getName());
                    tfPhone.setText(t.getPhoneNumber());
                    cbxGender.setSelectedIndex(t.isMale() ? 0 : 1);
                    tfBirthday.setText(t.getBirthday());
                    tfDepartment.setText(DepartmentBLL.getInstance().getNameById(t.getDepartmentId()));
                    tfDepartment.setEnabled(false);
                    btnResetPassword.addActionListener(new ActionListener() {
                        @Override
                        public void actionPerformed(ActionEvent e) {
                            if(UserBLL.getInstance().resetPassword(t.getId(), tfUsername.getText().toCharArray())) {
                                btnResetPassword.setText("Đặt lại thành công");
                                btnResetPassword.setEnabled(false);
                            }
                        }
                    });
                }
                case STUDENT -> {
                    Student t = (Student)o;
                    tfUsername.setText(UserBLL.getInstance().getUsername(t.getId()));
                    tfName.setText(t.getName());
                    tfPhone.setText(t.getPhoneNumber());
                    cbxGender.setSelectedIndex(t.isMale() ? 0 : 1);
                    tfBirthday.setText(t.getBirthday());
                    tfDepartment.setText(DepartmentBLL.getInstance().getNameById(StudentGroupBLL.getInstance().getStudentGroup(t.getStudentGroupId()).getDepartmentId()));
                    tfDepartment.setEnabled(false);
                    btnResetPassword.addActionListener(new ActionListener() {
                        @Override
                        public void actionPerformed(ActionEvent e) {
                            if(UserBLL.getInstance().resetPassword(t.getId(), tfUsername.getText().toCharArray())) {
                                btnResetPassword.setText("Đặt lại thành công");
                                btnResetPassword.setEnabled(false);
                            }
                        }
                    });
                }
            }
        } else {
            lblDepartment.setVisible(false);
            tfDepartment.setVisible(false);
            btnResetPassword.setEnabled(false);
        }
        
    }

