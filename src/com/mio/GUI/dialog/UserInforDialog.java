    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed

        rtn = new Object[2];
        switch (role) {
            case TEACHER -> {
                Teacher t = new Teacher();
                t.setName(tfName.getText());
                t.setPhoneNumber(tfPhone.getText());
                t.setMale(cbxGender.getSelectedIndex() == 0);
                t.setBirthday(tfBirthday.getText());
                t.setImagePath("");
                rtn[0] = t;
            }
            case STUDENT -> {
                Student t = new Student();
                t.setName(tfName.getText());
                t.setPhoneNumber(tfPhone.getText());
                t.setMale(cbxGender.getSelectedIndex() == 0);
                t.setBirthday(tfBirthday.getText());
                t.setImagePath("");
                rtn[0] = t;
            }
        }
        rtn[1] = tfUsername.getText();
        this.dispose();
    }//GEN-LAST:event_btnOkActionPerformed

